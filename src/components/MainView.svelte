        <h2>TL;DR - Hvordan var det å jobbe på Nordlandsbanen?</h2>
        I konklusjon har arbeidsforholdene på Nordlandsbanen variert i stor grad – Fra forsert tvangsarbeid under den tyske okkupasjonen, til et mer ordentlig arbeid etter 2. Verdenskrig der menneskerettighetene ikke ble brutt. Min oldefar, Øistein Orre Møller, har jobbet på Nordlandsbanen som blant annet skytebas, og han bodde for det meste i brakkebyer sammen med andre jernbanefolk. Han dro hjem til mormor samt familien i ferier og når han kunne, men fikk ikke se dem ofte, noe som dessverre var en realitet for mange jernbanearbeidere. Arbeidsdagene var lange og tunge, og arbeidet gikk trolig ut på ombygging og sikring av jernbanen, men også legging av skinner og tverliggende sviller til tider. I dag kan vi takke Øistein og resten av jernbanefolket for innsatsen deres i å bygge det Norge vi kjenner til i dag - en skinne om gangen :)
