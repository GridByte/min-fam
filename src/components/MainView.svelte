            Vi kan raskt starte å trekke linjer mellom det dårlige jernbanearbeidet og de dårlige arbeidsforholdene som fant sted i krigstiden. Mange av strekningene nord for Mosjøen ble forsert av Tyskerne, og sammen med forsultne og lite arbeidsdyktige krigsfanger, resulterte dette til dårlig kvalitet på blant annet banelegemet. Krigsfangene levde tett i brakkene, og mange døde på grunn av tvangsarbeidet, sult og henrettelser. Til tross for dette hevdes det at avstanden Mosjøen til Mo i Rana at «banelegemets kvalitet ble slett ikke dårlig» på grunn av at arbeidet for det meste ble ledet av nordmenn (Reklameboken for jernbanen i Nord-Norge (1953). Se kildeliste.).
