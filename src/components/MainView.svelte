    <div class="text-section">
        Arbeidsdagene var lange og harde, men arbeidet var ordentlig og NSB brøt ikke med menneskerettighetene. Livet i brakkebyene var enkelt - her spiste de, sov og gjorde av seg ellers, og de førte arbeidsbøker av god kvalitet hvor blant annet antall arbeidstimer ble nedskrevet. I desember 1947 strakte Nordlandsbanen seg allerede opp til Lønsdal, forbi vefsenområdet der Øistein hovedsakelig var stasjonert, og sjansen er derfor stor for at han drev med sikring og ombygging av banen. Dette var en viktig jobb fordi forholdene under krigen og den forserte jernbaneutbyggingen nordover for Mosjøen medførte en dårlig standard på fyllinger (jord og steinmasser) og på banelegemet på Nordlandsbanen.
