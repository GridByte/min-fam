            <h2>En mørk fortid</h2>
            I 1941, etter at tyskerne hadde okkupert Norge, begynte anleggsarbeidet å komme opp i et voldsomt tempo. Årsakene til den raske fremdriften var blant annet en enorm arbeidsstyrke på 26 000 mann, hvorav 16 000 av disse drev med jernbanearbeid mellom Mo i rana og Fauske, og bestod vesentlig av krigsfanger. Til sammenlikning var det knappe 800 mann som jobbet på strekningen Mo i rana til Bodø i 1955 (studieselsapets årsmøte. Se kildeliste). Av disse krigsfangene var omtrent 5 000 sovjetiske, mens resten var for det meste nordmenn og polakker. Den Tyske halvorganisasjonen OT (Organisation Todt), som drev med bygg- og anleggsvirksomhet, krevde 10 000 nordmenn, hvorav disse var fra Østlandet og ble stasjonert på Nordlandsbanen.
            <blockquote><q>«OT valgte folk på Østlandet med vilje. Da fikk de sendt dem lengst mulig unna der de bodde. Slik ble det vanskeligere å flykte»</q> - Gunnar D. Hatlehol (klarttale.no, se kildeliste)</blockquote>
        </div>
    </section>
    <div class="text-section">
        De forferdelige forholdene ved slaveanleggene, spesielt i Dunderdalen, over Saltfjellet og videre nordover, førte til store tap av menneskeliv (omtrent 2 000 menn), der 1 000 – 1 500 ble gravlagt langs banestrekningen. Av de 16 000 krigsfangene mellom Mo i Rana og Fauske, var 12 000 syke og forsultne, og arbeidet som ble gjennomført ble redusert av den grunn. Den Tyske byggeledelsen hadde dessuten skrevet at en tredjedel av fangene var syke, en tredjedel bare delvis arbeidsdyktige på grunn av sult, og bare den siste tredjedelen var arbeidsdyktige i februar 1945. Totalt sørget Tyskerne for å bygge 450 km på Nordlandsbanen, samt Sørlandsbanen, og som nevn tidligere, med svært høyt tempo. Etter krigen var NSB nødt for å bygge om mye av det tyskerne hadde gjennomført på Nordlandsbanen, spesielt strekningen mellom Storforshei og Lønsdal i årene 1945 – 1947. Vannomløp måtte rives og bygges om igjen, grøfter var helt forsømt, samt fjellkjæringer og sikring av jordskråninger var også ignorert. Tunneler var heller ikke sikret med skikkelig utstøpning, og det manglet plattformer på flere jernbanestasjoner (studieselsapets årsmøte. Se kildeliste).
