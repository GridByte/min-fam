            <h2>Øisteins arbeid på Nordlandsbanen</h2>
            <p>Nordlandsbanen begynte i 1902 på Hell, som ligger sør for Trondheim Lufthavn, og har siden den gang blitt etappevis forlenget helt opp til Bodø i 1962. I løpet av drøyt 60 år med konstruksjonsarbeid og ombygging har arbeidsforholdene på banen variert mye, blant annet på grunn av okkupasjonen under 2. verdenskrig. Da Øistein begynte å jobbe på Nordlandsbanen som ung voksen, var heldigvis krigen over, og NSB kunne drive konstruksjonsarbeidet selvstendig. Arbeidet til Øistein foregikk rundt omkring vefsnområdet, som ligger 6 til 10 mil fra Vega, og han måtte hovedsakelig jobbe om våren, sommeren og høsten. Til tross for snø og frost, kan det godt være at han arbeidet om vinteren også. Dette er fordi han blant annet drev med tunnelsprengning som skytebas, og kunne derfor oppholde seg inne i grunnfjellet hvor temperaturen var høyere og forholdene bedre enn utenfor.</p>
            Da Øistein først returnerte hjem på ferie, ble han værende bare en uke om gangen, og når han returnerte om vinteren, dro han ofte opp Nord på lofotfisket. Både på grunn av lofotfisket og jernbanearbeidet, tilbragte ikke Øistein mye tid sammen med mormor samt kjernefamilien, og han var ikke alene. I boka «Jernbanefolket forteller» forteller noen av datidens jernbanearbeidere om en vanskelig hussituasjon i Nord-Norge, og om brakkelivet langs anleggene. En av dem som det står om i boka, anleggsarbeider Ole Villman, kommenterte dette om brakkelivet: «Det var bra nok, men ikke det samme som å bo hjemme med sine egne». Hvis hussituasjonene hadde vært bedre for Øistein, hadde det kanskje vært mulig for hele familien å flytte nærmere jernbaneanleggene, og de kunne derfor tilbrakt mer tid sammen. Dessverre for Øistein og mange andre jernbanearbeidere, måtte de bo i såkalte brakkebyer utenfor jernbanevirksomheten. Han fikk derfor ikke vært mye sammen med familien sin, men på den andre siden tjente ham dem nokså godt.
            <blockquote><q>Penger var ikke et problem, og vi fikk alt vi trengte. Vi var ikke rike, men hadde nok til å klare oss.</q> – Karin Humstad (mormor)</blockquote>
