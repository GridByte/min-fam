            <h2>Min oldefar</h2>
            Øistein Orre Møller er min oldefar, og far til min Mormor. Han ble født 1. November 1920 og vokste opp på Vega, en stor øy ikke så langt fra Mosjøen. Der bodde han på en gård som het Vegmoen sammen med sine foreldre, Torstein Møller og Anna Johannessen, og sine søsken, Anders, Aase, Sigurd og Harald. På denne tiden var populasjonen på Vega vesentlig større enn det den er i dag (omtrent 3000 innbyggere i 1920 og 1200 i dag), og både skole og helsetjeneste var godt etablert. Ifølge intervju med Karin Humstad, min mormor, var Øistein dyktig på skolen. Han startet på skolen da han var seks år gammel og fikk gode karakterer, dessuten var han flink til å tegne, og var designeren bak mange av dekortegningene på skolen. Som ungdom drev han med lofotfisking, noe han dessverre ikke kunne fortsette med på grunn av en alvorlig ørebetennelse, og senere begynte han på fylkesskolen i Mosjøen. Da han returnerte til Vega i en alder på 22 til 24 år, giftet han seg med Jorunn møller, min oldemor, og etablert seg i nærheten av Vegmoen hvor han hadde vokst opp. Her fikk Øistein og Jorunn en datter, Karin, som nå er min mormor. Etter å ha fått jobb i Vegvesenet, noe som var sesongarbeid, startet han arbeidet på Nordlandsbanen. Her fulgte han sin fars 
            <q>togspor</q> 
            og holdt blant annet på med dynamittsprengning av grunnfjell og tunneler som skytebas.

